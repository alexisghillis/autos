package dto;

public class Version {
  private String modification;
  private String doors;
  private String power;
  private String typeCoupe;
  private String seats;
  private String yearBegin;
  private String yearEnd;

  public String getModification() {
    return modification;
  }

  public void setModification(String modification) {
    this.modification = modification;
  }

  public String getDoors() {
    return doors;
  }

  public void setDoors(String doors) {
    this.doors = doors;
  }

  public String getPower() {
    return power;
  }

  public void setPower(String power) {
    this.power = power;
  }

  public String getTypeCoupe() {
    return typeCoupe;
  }

  public void setTypeCoupe(String typeCoupe) {
    this.typeCoupe = typeCoupe;
  }

  public String getSeats() {
    return seats;
  }

  public void setSeats(String seats) {
    this.seats = seats;
  }

  public String getYearBegin() {
    return yearBegin;
  }

  public void setYearBegin(String yearBegin) {
    this.yearBegin = yearBegin;
  }

  public String getYearEnd() {
    return yearEnd;
  }

  public void setYearEnd(String yearEnd) {
    this.yearEnd = yearEnd;
  }
}
